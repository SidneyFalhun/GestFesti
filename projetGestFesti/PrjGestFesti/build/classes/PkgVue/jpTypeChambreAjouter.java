    private void jbAjouterTypeChambreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbAjouterTypeChambreMouseClicked
        // TODO add your handling code here:
        if (jfPrincipal.getSession().isOpen()){//jop  = new JOptionPane();
            if ((jtCode.getText().isEmpty()) || (jtLibelle.getText().isEmpty())){
                JOptionPane.showMessageDialog(null, "Les champs doivent être remplis", "Attention", JOptionPane.WARNING_MESSAGE);
            }
            else if (jtCode.getText().length() >= 3){
                JOptionPane.showMessageDialog(null, "Le code doit faire au plus 2 caractères", "Attention", JOptionPane.WARNING_MESSAGE);
            }
            else if (jtLibelle.getText().length() >= 15){
                JOptionPane.showMessageDialog(null, "La capacité ne doit pas faire plus de 15 caractères", "Attention", JOptionPane.WARNING_MESSAGE);
            }
            else{
                    // On récupère la saisie 
                String code = jtCode.getText().toString();
                String libelle = jtLibelle.getText().toString();
                    // On vérifie que ce code n'existe déjà pas
                String sReq = "From Typechambre Where TCH_ID = '" + code + "'";
                Query q = jfPrincipal.getSession().createQuery(sReq);
                    // Si c'est le cas
                if (q.uniqueResult() == null){
                        // Puis on insert dans la table
                    Typechambre uneTypeChambre = new Typechambre();
                    uneTypeChambre.setTchId(code);
                    uneTypeChambre.setTchLibelle(libelle);
                    Transaction tx = jfPrincipal.getSession().beginTransaction();
                    jfPrincipal.getSession().saveOrUpdate(uneTypeChambre);
                    jtCode.setText("");
                    jtLibelle.setText("");
                    tx.commit();
                    JOptionPane.showMessageDialog(null, "Ajout Effectué avec succès", "Information", JOptionPane.INFORMATION_MESSAGE);
                }
                else // Si le code existe
                {
                    JOptionPane.showMessageDialog(null, "Ajout impossible", "Attention", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jbAjouterTypeChambreMouseClicked

